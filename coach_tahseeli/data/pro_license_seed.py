def _shuffle_options(q_data):
    options = [
        ('A', q_data['a']),
        ('B', q_data['b']),
        ('C', q_data['c']),
        ('D', q_data['d']),
    ]
    correct_text = dict(options)[q_data['ans']]
    _random.shuffle(options)
    new_ans = next(letter for letter, text in options if text == correct_text)
    return {
        'a': options[0][1],
        'b': options[1][1],
        'c': options[2][1],
        'd': options[3][1],
        'ans': new_ans,
    }

