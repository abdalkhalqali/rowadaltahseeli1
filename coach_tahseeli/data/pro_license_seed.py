def seed_pro_license_questions():
    """بذر أسئلة الرخصة المهنية للفيزياء في قاعدة البيانات مع خلط الخيارات"""
    from models.pro_license_question import ProLicenseQuestion, PRO_STANDARDS
    from extensions import db

    existing = ProLicenseQuestion.query.count()
    if existing >= len(SEED_QUESTIONS):
        return f"الأسئلة محمّلة مسبقاً ({existing} سؤال)"

    added = 0
    for q in SEED_QUESTIONS:
        std_info = PRO_STANDARDS.get(q['standard'], {})
        shuffled = _shuffle_options(q)
        obj = ProLicenseQuestion(
            text         = q['q'],
            option_a     = shuffled['a'],
            option_b     = shuffled['b'],
            option_c     = shuffled['c'],
            option_d     = shuffled['d'],
            answer       = shuffled['ans'],
            explanation  = q.get('exp', ''),
            standard_num = q['standard'],
            standard_name= std_info.get('name', ''),
            q_type       = q['type'],
            difficulty   = q['difficulty'],
        )
        db.session.add(obj)
        added += 1

    db.session.commit()
    return f"تم إضافة {added} سؤال للرخصة المهنية (مع خلط الخيارات)"


def reshuffle_existing_questions():
    """إعادة خلط خيارات الأسئلة الموجودة في قاعدة البيانات"""
    from models.pro_license_question import ProLicenseQuestion
    from extensions import db

    questions = ProLicenseQuestion.query.filter_by(is_active=True).all()
    updated = 0
    for q in questions:
        options = [
            ('A', q.option_a),
            ('B', q.option_b),
            ('C', q.option_c),
            ('D', q.option_d),
        ]
        correct_text = dict(options).get(q.answer, q.option_a)
        _random.shuffle(options)
        new_ans = next((letter for letter, text in options if text == correct_text), 'A')
        q.option_a = options[0][1]
        q.option_b = options[1][1]
        q.option_c = options[2][1]
        q.option_d = options[3][1]
        q.answer   = new_ans
        updated += 1

    db.session.commit()
    return f"تم إعادة خلط خيارات {updated} سؤال بنجاح"
